'use client';

import { useState, useRef, useEffect } from 'react';
import type { Session, Summit } from '@/types/schedule';
import {
  generateGoogleCalendarUrl,
  generateICSContent,
  createSessionEvent,
} from '@/lib/calendar';

interface AddToCalendarProps {
  session: Session;
  date: string;
  summit: Summit;
}

export function AddToCalendar({ session, date, summit }: AddToCalendarProps) {
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    function handleClickOutside(event: MouseEvent) {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target as Node)) {
        setIsOpen(false);
      }
    }

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  const event = createSessionEvent(session, date, summit);

  const handleGoogleCalendar = () => {
    window.open(generateGoogleCalendarUrl(event), '_blank');
    setIsOpen(false);
  };

  const handleAppleCalendar = () => {
    const icsContent = generateICSContent(event);
    const blob = new Blob([icsContent], { type: 'text/calendar;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `${session.id}.ics`;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);
    setIsOpen(false);
  };

  const handleICSDownload = () => {
    const icsContent = generateICSContent(event);
    const blob = new Blob([icsContent], { type: 'text/calendar;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `${session.id}.ics`;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);
    setIsOpen(false);
  };

  return (
    <div className="relative" ref={dropdownRef}>
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="p-2 text-[var(--gray-light)] hover:text-[var(--flame)] transition-colors"
        aria-label="Add to calendar"
        aria-expanded={isOpen}
      >
        <CalendarIcon className="h-4 w-4" />
      </button>

      {isOpen && (
        <div className="absolute right-0 top-full mt-2 w-48 bg-[var(--dark)] border border-[var(--gray)] shadow-lg z-50">
          <div className="py-1">
            <button
              onClick={handleGoogleCalendar}
              className="w-full px-4 py-2 text-left font-mono text-xs hover:bg-[var(--black)] hover:text-[var(--flame)] flex items-center gap-3 transition-colors"
            >
              <GoogleIcon className="h-4 w-4" />
              Google Calendar
            </button>
            <button
              onClick={handleAppleCalendar}
              className="w-full px-4 py-2 text-left font-mono text-xs hover:bg-[var(--black)] hover:text-[var(--flame)] flex items-center gap-3 transition-colors"
            >
              <AppleIcon className="h-4 w-4" />
              Apple Calendar
            </button>
            <button
              onClick={handleICSDownload}
              className="w-full px-4 py-2 text-left font-mono text-xs hover:bg-[var(--black)] hover:text-[var(--flame)] flex items-center gap-3 transition-colors"
            >
              <DownloadIcon className="h-4 w-4" />
              Download .ics
            </button>
          </div>
        </div>
      )}
    </div>
  );
}

function CalendarIcon({ className }: { className?: string }) {
  return (
    <svg className={className} viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
      <rect x="3" y="4" width="18" height="18" rx="2" ry="2" />
      <line x1="16" y1="2" x2="16" y2="6" />
      <line x1="8" y1="2" x2="8" y2="6" />
      <line x1="3" y1="10" x2="21" y2="10" />
      <line x1="12" y1="14" x2="12" y2="18" />
      <line x1="10" y1="16" x2="14" y2="16" />
    </svg>
  );
}

